package Chapter10.Exercise;

import java.awt.Point;

public class Circle {
    private Point center;
    private Float radius;

    public Point getCenter() {
        return center;
    }

    public void setCenter(Point center) {
        this.center = center;
    }

    public Float getRadius() {
        return radius;
    }

    public void setRadius(Float radius) {
        this.radius = radius;
    }
}
